<script type="text/javascript">
  $(document).ready(function(){

    $("#simpan").click(function(){
      Get_id();
    });

    function Get_id(){
      $.ajax({
        type      : "GET",
        url       : "<?php echo base_url('batch/get_id_batch'); ?>",
        dataType  : "JSON",
        success   : function(data){
          kode ="BT";
          id = kode+data;
          $("#id_batch").val(id);
          save_batch();
        }
      });
    }

    function save_batch(){
      var id_batch = $("#id_batch").val();
      var tanggal = $("#tanggal").val();
      var no_batch = $("#no_batch").val();
      var no_suratjalan = $("#no_suratjalan").val();

      $.ajax({
        type      : "POST",
        url       : "<?php echo base_url('batch/tambah'); ?>",
        dataType  : "JSON",
        data      : {
                      id_batch: id_batch,
                      tanggal: tanggal,
                      no_batch: no_batch,
                      no_suratjalan: no_suratjalan
                    },
        success   : function(data){
          swal({
              title: "Simpan Batch Berhasil !",
              type: "success",
              confirmButtonColor: "#2C3FF9",
              showCancelButton: false,
              confirmButtonText: "OK",
              closeOnConfirm: true
            }, function(isConfirm) {
              Refresh();
          }); 
        }
      });
    }

    function Refresh(){
      document.getElementById('id_batch').value = "";
      document.getElementById('no_batch').value = "";
      document.getElementById('no_suratjalan').value = "";
      $('#no_batch').focus();
    }

  });
</script>